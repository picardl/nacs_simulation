function V = NaCsaPES(r)

wavenum2hartree = 4.55634011e-6;
bohr2angstrom = 0.529177210903;
r = r*bohr2angstrom;

r1 = 4.78;
r2 = 10.20;

A = -1.47429182e3;
B = 1.60029429e5;

a = [-217.1544 , 0.506003697108497441e1 , 0.471868074288336629e3 , ...
    0.251421896658343968e3, -0.874623764194756603e2, ...
    -0.215412551480785680e3, -0.237001094676986327e3, ...
    -0.597552181866083515e3, -0.476013801913782288e3 , ...
    0.378229284864475233e3, 0.679827008567800476e3, ...
    0.672327249787527307e3, -0.124756939670013161e2, ...
    -0.462561066282073739e3];
b = -0.64;
Rm = 5.75585938;

C6 = 1.555214e7;
C8 = 4.967239e8;
C10 = 1.971387e10;
Aex = 2.549087e4;
gamma = 5.12271;
beta = 2.17237;

U1 =@(r) A + B./r.^3;
U2 =@(r) sum(cell2mat(arrayfun(@(j) a(j)*((r-Rm)./(r+b*Rm)).^(j-1),reshape(1:numel(a),1,1,1,numel(a)),'un',0)),4);
U3 =@(r) -C6./r.^6 - C8./r.^8 - C10./r.^10 + Aex*(r.^gamma).*exp(-beta*r);

V = 0*r;
V(r<=r1) = U1(r(r<=r1));
V(r<r2 & r>r1) = U2(r((r<r2) & (r>r1)));
V(r>=r2) = U3(r(r>=r2));

V = V*wavenum2hartree;

end